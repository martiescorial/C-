using System;

namespace ConsoleApp3
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 10;
            Console.WriteLine(x);
            int y = 5;
            Console.WriteLine(y);
            Console.WriteLine("Suma = {0}", x + y);
            Console.WriteLine("Resta = {0}", x - y);
            Console.WriteLine("Multiplicación = {0}", x * y);
            Console.WriteLine("División = {0}", x / y);
            Console.WriteLine("Módulos = {0}", x % y);
        }
    }
}
